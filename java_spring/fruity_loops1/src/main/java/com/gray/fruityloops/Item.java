package com.gray.fruityloops;



public class Item {


//		Member variables
		private String name;
		private double price;
		
		
//		Constructor
		public Item(String name, double price) {
			this.name = name;
			this.price = price;
		}


		
		
//		Get name
		public String getName() {
			return name;
		}


//		Set Name
		public void setName(String name) {
			this.name = name;
		}


//		Get PriceNm
		public double getPrice() {
			return price;
		}

//		Set Price
		public void setPrice(double price) {
			this.price = price;
		}
		
		
		
		
		
		
}


